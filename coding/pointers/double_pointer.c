#include <stdio.h>

int main()
{
    int a[10] = {100,200,300,400,500,600,700,800,900,1000};
    
    int *p[]= {a,a+1,a+2,a+3,a+4,a+5,a+6,a+7,a+8,a+9};
    
    int **ptr = p;
    printf("(&a+1)= %u\t(a)= %u\t(*a)=%u\n",&a+1,a,*a);
    printf("###########################################\n");
    printf("(&p+1)= %u\t(p)= %u\t(*p)=%u\t(**p)=%u\n",&p+1,p,*p,**p);
    printf("###########################################\n");
    printf("(&ptr)= %u\t(ptr)= %u\t(*ptr)= %u\t(**ptr)= %u\n",&ptr+1,ptr,*ptr,**ptr);
    ptr++;
    printf("(&ptr)= %u\t(ptr)= %u\t(*ptr)= %u\t(**ptr)= %u\n",&ptr,ptr,*ptr,**ptr);
    printf("###########################################\n");
    printf("%lu\t%lu\t%d\n",ptr-p,*ptr-a,**ptr);
    printf("###########################################\n");
    printf("%u\t%u\n",ptr,*ptr);
    ++*ptr;
    printf("%u\t%u\n",ptr,*ptr);
    printf("%lu\t%lu\t%d\n",ptr-p,*ptr-a,**ptr);
    ++**ptr;
    printf("%lu\t%lu\t%d\n",ptr-p,*ptr-a,**ptr);
    printf("%u\t%u\n",ptr,*ptr);
    return 0;
}

